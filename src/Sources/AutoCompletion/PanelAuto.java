    public void setDialogueEtListe(List<String> myWords, PROPRIETE prop){
        this.dialog = new DialAuto(new javax.swing.JFrame(), true, myWords, icone);
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        Xsouris = ((int)dim.getWidth()/2)+300;
        Ysouris = ((int)dim.getHeight()/2)-100;
        dialog.setTitle(prop.getNom()+" - Recherche");
        this.prop = prop;
        AfficherComboAuto(Xsouris, Ysouris);
    }
    
