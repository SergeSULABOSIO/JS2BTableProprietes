    private void AfficherComboAuto(int x, int y) {
        dialog.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosing(java.awt.event.WindowEvent e) {
                dialog.dispose();
            }
        });
        dialog.setBounds(x-dialog.getWidth(), y, 400, 95);
        dialog.setVisible(true);
        if(dialog.getSeletedItem() == null){
            jLabel1.setText(prop.getValeurs().firstElement()+"");
        }else{
            jLabel1.setText(dialog.getSeletedItem()+"");
        }
        prop.setValeurSelectionne(dialog.getSeletedItem());
    }

